// primitive DataType
var a = 10;
console.log(a);
console.log(typeof a);

var b = "hello";
var b = "Gaurav";
var b = "A";

console.log(b);
console.log(typeof b);

var c = true;
console.log(c);
console.log(typeof c);

var d;
console.log(d);
console.log(typeof d);

var e = null;
console.log(e);
console.log(typeof e);

// Complex DataType

// function
function greet() {
    console.log("Hello Gaurav");
}
greet();

// Array
let number = [1,2,3,5,4];
console.log(number);
console.log(number[3]);

// Object
let person = {
    id: 101,
    name: "Gaurav",
    age: 22
};
console.log(person);
console.log(person.name);
console.log(person.age);
console.log(person.id);


